$Sim::Config = {
    'shops' => {},
    'pages' => {},
};

sub gen_shop {
    my $id = shift;
    my $shop = $Sim::Config->{shops};
    my $baseurl = "http://shop$id.taobao.com";
    my $homepage = $baseurl;
    my $new = {
        id => $id,
        beacon => $id.'.gif',
        pages => {
            $baseurl => 0,
        },
    };
    my $pages = $new->{pages};
    for ( 1..rand(100) ) {
        $pages->{ get_item_page($baseurl, md5_hex(rand())) } = 0;
    }
    for ( 1..rand(10) ) {
        $pages->{ get_taoke_item_page( md5_hex(rand()) ) } = 0;
    }
    return $new;
}

sub get_item_page {
    my ($base, $item_id) = @_;
    my $u = URI->new($base);
    $u->query_form({'itemid' => $item_id});
    return $u;
}

sub get_taoke_item_page {
    my ($taomi_id) = @_;
    my $u = URI->new("http://item.taobao.com/auction/item_detail--.jhtml");
    $u->query_form({'taomi' => $taomi_id});
    return $u;
}

sub gen_config {
    for ( 1..10 ) {
        $Sim::Config->{shops}{$_} = gen_shop( $_ );
    }
    my $pages = {};
    foreach my $shop ( values %{$Sim::Config->{shops}} ) {
        my $id = $shop->{id};
        foreach ( keys %{$shop->{pages}} ) {
            $pages->{$_} = $id;
        }
    }
    $Sim::Config->{pages} = $pages;
}

sub get_beacon_by_url {
    my $self = shift;
    my $url = shift;
    my $shop = $self->get_shop_by_url($url);
    return if !defined $shop;
    return '/beacon/?' . $shop->{id}. '.gif';
}

sub get_shop_by_url {
    my $self = shift;
    my $url = shift;
    my $id = $Sim::Config->{pages}{$url};
    if ( defined $id ) {
        return $Sim::Config->{shops}{$id};
    }
}

sub get_shop_by_beacon {
    my $self = shift;
    my $url = shift;
    if (  my ($shop_id) = ($url =~ /beacon\/\?(\d+)\.gif/) ) {
        return $Sim::Config->{shops}{$shop_id};
    } 
}

