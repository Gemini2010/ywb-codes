package Sim::Config;
use Data::Dumper qw(Dumper);
use Digest::MD5 qw/md5_hex/;

$Sim::Config = {
    'pixels_count' => 0,
    'shops' => [
    ]
};

sub gen_shop {
    my $shop = $Sim::Config->{shops};
    my $id = scalar(@$shop);
    my $homepage = "http://shop$id.taobao.com";
    my $new = {
        id => $id,
        pixels => [
            ++$Sim::Config->{pixels_count} . '.gif',
        ],
        pages => {
            $homepage => 0,
        },
    };
    my $pages = $new->{pages};
    for ( 1..rand(100) ) {
        $pages->{$homepage . '?itemid=' . md5_hex(rand())} = 0;
    }
    return $new;
}

sub gen_config {
    for ( 1..10 ) {
        push @{$Sim::Config->{shops}}, gen_shop();
    }

    my $pages = {};
    my $pixels = {};
    foreach my $shop ( @{$Sim::Config->{shops}} ) {
        my $id = $shop->{id};
        foreach ( @{$shop->{pixels}} ) {
            $pixels->{$_} = $id;
        }
        foreach ( keys %{$shop->{pages}} ) {
            $pages->{$_} = $id;
        }
    }
    $Sim::Config->{pages} = $pages;
    $Sim::Config->{pixels} = $pixels;
}

sub get_pixel_by_url {
    my $self = shift;
    my $url = shift;
    my $shop = $self->get_shop_by_url($url);
    return if !defined $shop;
    my $pixels = $shop->{pixels};
    my $pixel_id = $shop->{pages}{$url};
    if ( defined $pixel_id ) {
        return $pixels->[$pixel_id];
    }
}

sub get_shop_by_url {
    my $self = shift;
    my $url = shift;
    my $shops = $Sim::Config->{shops};
    my $id = $Sim::Config->{pages}{$url};
    if ( defined $id ) {
        return $shops->[$id];
    }
}

sub get_shop_by_pixel {
    my $self = shift;
    my $url = shift;
    my $shops = $Sim::Config->{shops};
    my $id = $Sim::Config->{pixels}{$url};
    if ( defined $id ) {
        return $shops->[$id];
    }
}

